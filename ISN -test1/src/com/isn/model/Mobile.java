package com.isn.model;

public class Mobile {
	private int productId = 0;
	private String name = null;
	private String description = null;
	private float price = 0;
	private String productImage = null;
	private int productScreenSize = 0;
	private int ram = 0;
	private String os = null;
	private int screenWidth = 0;
	
	public int getProductId() {
		return productId;
	}
	public void setProductId(int productId) {
		this.productId = productId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public String getProductImage() {
		return productImage;
	}
	public void setProductImage(String productImage) {
		this.productImage = productImage;
	}
	public int getProductScreenSize() {
		return productScreenSize;
	}
	public void setProductScreenSize(int productScreenSize) {
		this.productScreenSize = productScreenSize;
	}
	public int getRam() {
		return ram;
	}
	public void setRam(int ram) {
		this.ram = ram;
	}
	public String getOs() {
		return os;
	}
	public void setOs(String os) {
		this.os = os;
	}
	public int getScreenWidth() {
		return screenWidth;
	}
	public void setScreenWidth(int screenWidth) {
		this.screenWidth = screenWidth;
	}

	
}
